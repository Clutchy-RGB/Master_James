#include <unistd.h>

int ft_is_prime(int nb)
{
	int a;

	if (nb == 2)
		return (1);
	if (nb <=1 || (nb % 2) == 0)
		return (0);
	a = 3;
	while(a <= nb / a)
	{
		if ((nb % a) == 0)
			return (0);
		a += 2;
	}
	return (1);
}
