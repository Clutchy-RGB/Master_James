#include <unistd.h>

int	ft_sqrt(int nb)
{
	int a;

	a = 0;
	if ( nb <= 0)
		return (0);
	while (a <= nb / 2)
	{
		if (a * a == nb)
			return (a);
			a++;
	}
	return (0);
}
