#include	<unistd.h>

int	ft_atoi(char *str)
{
	int	a;
	int	b;
	int	out;

	a = 0;
	b = 1;
	out = 0;
	while (str[a] >= '0' && str[a] <= '9')
	{
		out *= 10;
		out += str[a] - '0';
		a ++;
	}
	return (out);
}

